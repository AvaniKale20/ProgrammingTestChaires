package com.benchire.calculatePrice.parts;

public class CycleParts {
    private Tyre tyre;
    private Gear gears;
    private String frame;
    private int breaks;
    private int seat;

    public CycleParts(Tyre tyre, Gear gears, String frame, int breaks, int seat) {
        this.tyre = tyre;
        this.gears = gears;
        this.frame = frame;
        this.breaks = breaks;
        this.seat = seat;
    }


}
